    private void txtPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPesquisaActionPerformed
String nome;
        int i;
        nome = txtPesquisa.getText();
        i = bdC.pesquisar(nome);
        if (i != -1) {
            txtLista.setText("     Resultado da Pesquisa realizada     "
                    + "\n=======================================\n\n"
                    + (i + 1) + " - " + nome.toUpperCase());
        } else if (bdC.retornarVazio()) {
            txtLista.setText("     Relação de Dados vazia!!     ");

        } else {
            txtLista.setText("     Resultado da Pesquisa realizada     "
                    + "\n=======================================\n\n"
                    + "Este Componente não foi encontrado!");
        }                                          
    }//GEN-LAST:event_txtPesquisaActionPerformed

