    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
     String nome, novoNome;
        int i;
        nome = txtEditaExclui.getText().toUpperCase();
        i = bdC.pesquisar(nome);
        
        if (i != -1) {
           novoNome = JOptionPane.showInputDialog("Informe o nome do Item").toUpperCase();
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja alterar este Item?",
                    "Alterar", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                bdC.editar(i,novoNome);
                btListar.doClick();
            }
        } else if (bdC.retornarVazio()) {
            txtLista.setText("     Relação de Itens vazia!!     ");

        } else {
            txtLista.setText("     Resultado da Pesquisa realizada     "
                    + "\n=======================================\n\n"
                    + "Este item não foi encontrado!");
        }

        btExcluir.setVisible(false);
        btEditar.setVisible(false);
        txtEditaExclui.setVisible(false);
        txtBuscar.setVisible(false);
    }//GEN-LAST:event_btEditarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
    String nome;
        int i;
        nome = txtEditaExclui.getText().toUpperCase();

        i = bdC.pesquisar(nome);
        if (i != -1) {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir este Item?",
                    "Excluir", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                bdC.excluir(i);
                txtLista.setText("Item Excluído com Sucesso!!");
            }
        } else if (bdC.retornarVazio()) {
            txtLista.setText("     Relação de Item vazia!!     ");

        } else {
            txtLista.setText("     Resultado da Pesquisa realizada     "
                    + "\n=======================================\n\n"
                    + "Este Item não foi encontrado!");
        }

        btExcluir.setVisible(false);
        btEditar.setVisible(false);
        txtEditaExclui.setVisible(false);
        txtBuscar.setVisible(false);
    }//GEN-LAST:event_btExcluirActionPerformed

